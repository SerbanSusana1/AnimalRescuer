package org.fasttrackit;

public class Animal {

  private String name;
  private double age;
  private double health;
  private double hangerLevel;
  private double mindset;
  private   String animalFood;
  private   String activities;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getAge() {
        return age;
    }

    public void setAge(double age) {
        this.age = age;
    }

    public double getHealth() {
        return health;
    }

    public void setHealth(double health) {
        this.health = health;
    }

    public double getHangerLevel() {
        return hangerLevel;
    }

    public void setHangerLevel(double hangerLevel) {
        this.hangerLevel = hangerLevel;
    }

    public double getMindset() {
        return mindset;
    }

    public void setMindset(double mindset) {
        this.mindset = mindset;
    }

    public String getAnimalFood() {
        return animalFood;
    }

    public void setAnimalFood(String animalFood) {
        this.animalFood = animalFood;
    }

    public String getActivities() {
        return activities;
    }

    public void setActivities(String activities) {
        this.activities = activities;
    }
}
